function cluster () {

    var nodes,
      strength = 0.1;
  
    function force (alpha) {
  
      // scale + curve alpha value
      alpha *= strength * alpha;
  
      nodes.forEach(function(d) {
              var cluster = clusters[d.cluster];
          if (cluster === d) return;
        
        let x = d.x - cluster.x,
          y = d.y - cluster.y,
          l = Math.sqrt(x * x + y * y),
          r = d.radius + cluster.radius;
  
        if (l != r) {
          l = (l - r) / l * alpha;
          d.x -= x *= l;
          d.y -= y *= l;
          cluster.x += x;
          cluster.y += y;
        }
      });
  
    }
  
    force.initialize = function (_) {
      nodes = _;
    }
  
    force.strength = _ => {
      strength = _ == null ? strength : _;
      return force;
    };
  
    return force;
  
  }